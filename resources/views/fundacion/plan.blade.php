                <h3>2021, año del Buey</h3>
                <h4>Objetivo principal: romperse el lomo</h4>
                <ol>
                    <li>Actualizar y hacer crecer la base de datos formada por lectores, periodistas, narradores,
                        ilustradores, cineastas, guionistas y empresas ligadas con la cultura que aportan fondos y
                        difunden las actividades de FUNDACIÓN ORSAI, con el objetivo de hacer sustentable cada proyecto
                        y ampliar los convenios con otras fundaciones, instituciones o empresas afines al ámbito
                        cultural y educativo.
                    </li>
                    <br/>
                    <li>Llamar a concurso a arquitectos, ingenieros de luz y sonido, directores de teatro, empresas de
                        gastronomía, hostelería, turismo y a la comunidad en general para la creación de diversos
                        emprendimientos en los diversos pabellones y espacios abiertos del ex Instituto Capitán
                        Sarmiento (ubicado en Olivera, Partido de Luján).
                    </li>
                    <br/>
                    <li>Dar inicio a uno de los pilares del segundo año: la habilitación al público —de modo gradual— de
                        un enorme Polo de Cultura y Arte (pionero en Latinoamérica) llamado &laquo;CIUDAD CULTURA ORSAI&raquo;,
                        que presentará opciones de conciertos de música y arte, exposiciones itinerantes, hospedaje para
                        los visitantes, guardería, restaurantes, talleres, becas de trabajo literario, centro de
                        convenciones culturales, set para filmación de películas de cine o televisión y todas las
                        actividades que puedan desarrollarse en un predio de más de 400 hectáreas de llanura, con el
                        objetivo de convertirlo en un destino turístico natural para cualquiera que desee visitar la
                        Argentina.
                    </li>
                    <br/>
                    <li>Mantener, afianzar y dar rango de tradición a las segundas ediciones de los &laquo;concursos
                        transparentes&raquo; creados durante el primer año. El gran caballito de batalla deberá ser el
                        2º Concurso Hispanoamericano de Cuentos Sin Jurado, en donde los benefactores y adherentes de
                        FUNDACIÓN ORSAI votan, de manera online y en en un formato de reality, los cuentos presentados,
                        provocando que el premio (la dotación económica) crezca de modo exponencial conforme haya más
                        jurados populares.
                    </li>
                    <br/>
                    <li>Imprimir libros y revistas en papel (siempre con su correlato digital) de las obras e
                        investigaciones que hayan resultado premiadas en los diferentes concursos organizados por
                        FUNDACIÓN ORSAI y poner estas obras en circulación comercial y académico.
                    </li>
                    <br/>
                    <li>Mantener, afianzar y dar rango de tradición a las segundas ediciones de los concursos
                        internacionales de Investigación (donde los participantes proponen temáticas de investigación
                        literaria o periodística de gran complejidad económica) y las Becas de Postgrado (donde los
                        postulantes le relatan a la comunidad su deseo de finalizar sus estudios).
                    </li>
