                <h3>2022, año del Tigre</h3>
                <h4>Objetivo principal: dar el zarpazo</h4>
                <ol>
                    <li>Propender a que FUNDACIÓN ORSAI, desde el inicio de su tercer año de vida, pueda sostener
                        económicamente sus proyectos sin dependencia (ni exclusiva ni parcial) del Municipio de Mercedes
                        ni de otro organismo del Estado Nacional, o al menos no de manera excluyente.
                    </li>
                    <br/>
                    <li>Inaugurar y habilitar al público —de modo completo— el polo de cultura y arte más grande de
                        América Latina, llamado &laquo;CIUDAD CULTURA ORSAI&raquo;: un predio de más de 400 hectáreas de
                        llanura (con doce pabellones techados de gran tamaño) dedicados al arte narrativo en todas sus
                        variantes, con el objetivo de convertirlo en un destino turístico natural para cualquiera que
                        desee visitar la Argentina.
                    </li>
                    <br/>
                    <li>Generar cronogramas de actividades unificadas entre la sede de Mercedes y la de Luján, de tal
                        modo de que existan diferentes actividades permanente en una u otra sede, poniendo cada vez
                        mayor esfuerzo en la universalización de los eventos en todas las regiones donde se hable el
                        idioma castellano.
                    </li>
                    <br/>
                    <li>Mantener, afianzar y dar rango de tradición a todos los proyectos trimestrales, semestrales y
                        anuales creados en los dos primeros años de FUNDACIÓN ORSAI, con especial cuidado en los rubros
                        de comunidad, investigación y docencia.
                    </li>
                    <br/>
                    <li>Acrecentar la publicación, el tiraje, la comercialización y la calidad conceptual y gráfica de
                        los trabajos literarios y de investigación que surjan de los concursos, talleres y las demás
                        actividades de FUNDACIÓN ORSAI.
                    </li>
                    <br/>
                    <li>Propender a que la dotación económica de cada concurso o beca sea cada año mayor, que beneficie
                        a cada vez más postulantes, que los trabajos presentados crezcan en cantidad y calidad, y que
                        los porcentajes de participantes no argentinos sea cada vez mayor.
                    </li>
                    <br/>
                    <li>Renovar, con calidad de anual o semestral, el &laquo;Gran Encuentro Nacional de Profesores de
                        Literatura con Escritores Vivos en Estado Salvaje&raquo;, consolidar los puntajes a docentes en
                        todas las provincias, abrir estos encuentros a otros ámbitos y generar convenios y ayudas para
                        que puedan realizarse también en otros países de habla hispana.
                    </li>
                    <br/>
                    <li>Ofrecer becas para la creación de obras de largo aliento (novelas, obras de teatro, guiones de
                        cine) a escritores y narradores de cualquier región en los hospedajes de la &laquo;Ciudad
                        Cultural Orsai&raquo;, con todos los gastos pagados, con el fin de publicar la obra resultante.
                    </li>
                    <br/>
                    <li>Y finalmente: pensar, producir y ejecutar de manera constante cursos, talleres, concursos y
                        eventos relacionados con el arte de la narrativa que cumplan con el espíritu de Orsai: juntar
                        cada vez a más gente para contar y escuchar historias en un mismo idioma.
                    </li>
                    <br/>
                </ol>
