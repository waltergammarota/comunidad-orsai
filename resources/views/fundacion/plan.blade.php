                    <li>Crear un sistema de concursos (para la comunidad de narradores, para el desarrollo de la
                        investigación en literatura y para la asignación de becas de formación de posgrado) que proponga
                        una nueva manera, limpia y clara, de dotar de recompensas económicas de gran valor a autores,
                        narradores, investigadores, docentes y estudiantes de literatura.
                    </li>
